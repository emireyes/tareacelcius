#include <stdio.h>

	int main () {
		
	float temperaturaFahrenheit;
	float temperaturaCelsius;	
	
	float celsius= 18;			
	float fahrenheit= 86;
	temperaturaFahrenheit=(9/5.0)*celsius+32;
	temperaturaCelsius=(fahrenheit-32.0)*(5.0/9.0);
	
	printf("temperaturaFahrenheit: %f \n",temperaturaFahrenheit);
	printf("temperaturaCelsius %f \n",temperaturaCelsius);
	return 0;
		
			
		
	}
	
	




